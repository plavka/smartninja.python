mood = input("How do you feel today? ")

if mood == "happy":
    print("It is great that you are happy.")
elif mood == "sad":
    print("I am sorry to see you sad.")
elif mood == "nervous":
    print("Take a deep breath three times.")
elif mood == "relaxed":
    print("Great to see you relaxed.")
elif mood == "excited":
    print("Great, what are you excited about?")

else:
    print("I don't recognize this mood")